namespace Presque\DependencyInjection\Compiler;

use Symfony\Component\DependencyInjection\Reference,
    Symfony\Component\DependencyInjection\ContainerBuilder,
    Symfony\Component\DependencyInjection\Compiler\CompilerPassInterface;

class ConsoleCommandsPass implements CompilerPassInterface
{
    /**
     * Processes container.
     *
     * @param ContainerBuilder $container
     */
    public function process(ContainerBuilder $container)
    {
        $commands = array();

        foreach ($container->findTaggedServiceIds('presque.command') as $id => $attributes) {
            $commands[] = new Reference($id);
        }

        $container->setParameter('presque.commands', $commands);
    }
}